console.log("Caso con ++b -> Incrementa el valor de a en 1 y despues devuelve su contenido");
let b=5;
console.log(++b);
console.log(b);

console.log("");
console.log("Caso con c-- -> Devuelve su contenido y despues lo decrementa en 1");
let c=5;
console.log(c--);
console.log(c);

console.log("");
console.log("Caso con --d -> Decrementa el valor de a en 1 y despues devuelve su contenido");
let d=5;
console.log(--d);
console.log(d);